"""Кастомные исключения ядра. Сообщения — для пользователя, не для дебага."""


class ARHudError(Exception):
    """Базовое исключение проекта."""


class CameraInitError(ARHudError):
    """Не удалось инициализировать источник видео."""


class ModuleInitError(ARHudError):
    """Не удалось инициализировать модуль из конфига."""


class ConfigError(ARHudError):
    """Сломанный/неполный конфиг. Падаем на старте, не в середине рендера."""